using DesignPatterns2.Cap8;
using System;

namespace DesignPatterns2.Cap9
{
    public  class FacadeCompany
    {
        public Client GetClient(string document)
        {
            return new ClientDAO.GetByDocument();
        }

        public Invoice CreateInvoice(Client client, double value)
        {
            return new Invoice(client, value);
        }
    
        public Charge CreateCharge(Type type, Invoice invoice)
        {
            Charge charge = new Charge(type, invoice);
            charge.Issue();
            return charge;
        }

        public Contact GetContact(Client client, Invoice invoice)
        {
            Contact contact = new Contact(client, invoice);
            contact.Send();
            return contact;
        }
    }
}
